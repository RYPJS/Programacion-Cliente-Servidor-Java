    private void BbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BbuscarActionPerformed
    if (TAorigen.getText().isEmpty() || TAdestino.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Uno o m√°s campos vac√≠os", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }
    else{
        formatoFecha.setLenient(false); // Evita fechas inv√°lidas
        try {
            formatoFecha.parse(TAfechaViaje.getText());
            JOptionPane.showMessageDialog(null, "test");
            //Continuar ac√°
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Formato de fecha inv√°lido. Asegurese que cumpla con dd/mm/yyyy", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }       
    }

    
    }//GEN-LAST:event_BbuscarActionPerformed

