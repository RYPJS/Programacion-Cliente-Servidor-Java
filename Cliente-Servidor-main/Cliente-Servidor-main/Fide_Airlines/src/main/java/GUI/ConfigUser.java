    private void bntGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bntGuardarMouseClicked
        try {
            //Conexion con la base de datos
            Connection nuevaConexion = DriverManager.getConnection("jdbc:mysql://localhost/fide_airlines?serverTimezone=UTC","root","12345");
            
            String SQLUpdate="UPDATE usuario SET nombre= ?,identificacion= ?,correo= ?,contrasenia= ?,fechaNacimiento= ? WHERE identificacion = ?";
            PreparedStatement SQLUpdateP = nuevaConexion.prepareStatement(SQLUpdate);
            
            //Definimos los parametros
            SQLUpdateP.setString(1,txtNombre.getText());
            SQLUpdateP.setString(2,txtCedula.getText());
            SQLUpdateP.setString(3,txtCorreo.getText());
            SQLUpdateP.setString(4,txtContrasenia.getText());
            SQLUpdateP.setString(5,txtFnacimiento.getText());
            SQLUpdateP.setString(6,ident);
            
            SQLUpdateP.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"El registro fue actualizado de manera correcta");
            
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this,"Error" + error.getMessage());
        }
    }//GEN-LAST:event_bntGuardarMouseClicked

