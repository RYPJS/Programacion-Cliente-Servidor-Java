    public ConfigUser(String identificacion) {
        initComponents();
        ident = identificacion;
        try {
         Connection nuevaConexion = DriverManager.getConnection("jdbc:mysql://localhost/fide_airlines?serverTimezone=UTC", "root", "12345");
         String SQLBuscar = "SELECT nombre,identificacion,correo,contrasenia,fechaNacimiento FROM usuario WHERE identificacion = ?";
         PreparedStatement SQLBuscarP = nuevaConexion.prepareStatement(SQLBuscar);
         
         
         SQLBuscarP.setString(1,ident);
            
         ResultSet resultado = SQLBuscarP.executeQuery();
         
         if (resultado.next()){
                txtNombre.setText(resultado.getString("nombre"));
                txtCedula.setText(resultado.getString("identificacion"));
                txtCorreo.setText(resultado.getString("correo"));
                txtContrasenia.setText(resultado.getString("contrasenia"));
                txtFnacimiento.setText(resultado.getString("fechaNacimiento"));
            }else{
                JOptionPane.showMessageDialog(this, "No fue posible encontrar el registro buscado");
            }
        } catch (Exception error) {
         JOptionPane.showMessageDialog(this, "Error" + error.getMessage());        
        }
    }

